package org.unindra.tabungan.models;

public class Nasabah {
    private int id_nasabah;
    private int nama_nasabah;

    public int getId_nasabah() {
        return id_nasabah;
    }

    public void setId_nasabah(int id_nasabah) {
        this.id_nasabah = id_nasabah;
    }

    public int getNama_nasabah() {
        return nama_nasabah;
    }

    public void setNama_nasabah(int nama_nasabah) {
        this.nama_nasabah = nama_nasabah;
    }
}
